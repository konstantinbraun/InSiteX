using Resources;
using System;
using System.Linq;

namespace InSite.App.Constants
{
    public static class Actions
    {
        public const int None = 0;
        public const int View = 1;
        public const int Create = 2;
        public const int Edit = 3;
        public const int Delete = 4;
        public const int Release = 5;
        public const int Lock = 6;
        public const int Reject = 7;
        public const int ApplyFor = 8;
        public const int Move = 9;
        public const int Unlock = 10;
        public const int Print = 11;
        public const int Activate = 12;
        public const int Deactivate = 13;
        public const int Assign = 14;
        public const int ReleaseBp = 15;
        public const int Insert = 16;
        public const int Update = 17;

        public static string GetActionString(int status)
        {
            string actionString = "";

            switch (status)
            {
                case View:
                    actionString = Resource.lblActionView;
                    break;
                case Create:
                    actionString = Resource.lblActionCreate;
                    break;
                case Edit:
                    actionString = Resource.lblActionEdit;
                    break;
                case Delete:
                    actionString = Resource.lblActionDelete;
                    break;
                case Release:
                    actionString = Resource.lblActionRelease;
                    break;
                case Lock:
                    actionString = Resource.lblActionLock;
                    break;
                case Reject:
                    actionString = Resource.lblActionReject;
                    break;
                case ApplyFor:
                    actionString = Resource.lblActionApplyFor;
                    break;
                case Move:
                    actionString = Resource.lblActionMove;
                    break;
                case Unlock:
                    actionString = Resource.lblActionUnlock;
                    break;
                case Print:
                    actionString = Resource.lblActionPrint;
                    break;
                case Activate:
                    actionString = Resource.lblActionActivate;
                    break;
                case Deactivate:
                    actionString = Resource.lblActionDeactivate;
                    break;
                case Assign:
                    actionString = Resource.lblActionAssign;
                    break;
                case ReleaseBp:
                    actionString = Resource.lblReleaseForBP;
                    break;
                case Insert:
                    actionString = Resource.lblActionInsert;
                    break;
                case Update:
                    actionString = Resource.lblActionUpdate;
                    break;
            }

            return actionString;
        }
    }
}